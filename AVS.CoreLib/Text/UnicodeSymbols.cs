namespace AVS.CoreLib.Text;

/// <summary>
/// Utility class that provides some frequently used Unicode symbols as string constants.
/// </summary>
public static class UnicodeSymbols
{
    /// <summary>
    /// Up arrow symbol (↑)  
    /// </summary>
    public const string ARROW_UP = "\u2191";
    /// <summary>
    /// Down arrow symbol (↓)
    /// </summary>
    public const string ARROW_DOWN = "\u2193";
    /// <summary>
    /// Left arrow symbol (←)
    /// </summary>
    public const string ARROW_LEFT = "\u2190";
    /// <summary>
    /// Right arrow symbol (→)
    /// </summary>
    public const string ARROW_RIGHT = "\u2192";
    /// <summary>
    /// Check mark symbol (✓)
    /// </summary>
    public const string CHECK = "\u2713";
    /// <summary>
    /// Cross mark symbol (✗)
    /// </summary>
    public const string CROSS = "\u2717";
}